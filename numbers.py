# Get user input for three integers
num1 = int(input("Enter the first integer: "))
num2 = int(input("Enter the second integer: "))
num3 = int(input("Enter the third integer: "))
# Calculate and print the requested values
print(f"1) Sum of all numbers:{num1 + num2 + num3}")
print(f"2) First number minus second number: {num1-num2}")
print(f"3) Third number multiplied by first number: {num3 * num1}")
print(f"4) Sum of all three numbers divided by the third number: {(num1 + num2 + num3) / num3}")